<div class="header"><h2>Libre Graphics Meeting : Communiqué de Presse</h2></div>
<hr />

<h3>17-18-19 mars 2006 : Le meilleur de l'imagerie numérique libre à l'occasion du Libre Graphics Meeting à CPE Lyon</h3>

<p>Retouche photo, création, rendering et animation 3D, dessin vectoriel, PAO : dans tous les domaines de l'imagerie numérique, il existe des solutions logicielles libres (et donc utilisables gratuitement et sans contraintes) qui s'imposent de plus en plus face aux solutions "classiques" des grands éditeurs logiciels.</p>

<p>C'est pour cela que des produits tels que GIMP, Blender, Inkscape ou encore Scribus seront à l'honneur lors de la conférence internationale "Libre Graphics Meeting", les 17, 18 et 19 mars à CPE Lyon où plus de 200 personnes, utilisateurs et grand public, sont attendues pour découvrir les innovations apportées par ces logiciels et rencontrer les développeurs.</p>

<h3>En avant-première mondiale : Elephant's dream, le premier film d'animation libre</h3>

<p>Issu du Projet Orange, "Elephant's Dream" est le premier court-métrage d'animation 3D entièrement réalisé grâce à des logiciels libres et lui-même diffusé selon une licence garantissant une libre utilisation et une libre distribution à tout un chacun.</p>

<p>C'est dans la ville des frères Lumière, à CPE Lyon, l'école où Louis-Auguste Lumière a étudié, dans le cadre du Libre Graphics Meeting, que cette nouvelle production pionnière dans l'histoire du cinéma sera diffusée en avant-première mondiale avant d'être proposée aux grands festivals internationaux (Siggraph Theatre, Festival de Cannes, Dutch Animation Festival, ...)</p>

<p>Organisée par les développeurs du GIMP, CPE Lyon et l'ALDIL, le Libre Graphics Meeting est soutenu par HP, Xara, O'Reilly, Flock, FSF, la fondation Gnome, Linux Magazine France et Eyrolles.</p>

<h3>A propos des organisateurs</h3>

<p>Le GIMP, Inkscape et Scribus sont des outils d'infographie qui évoluent très rapidement et qui concurrencent les leaders du marché comme Adobe ou Corel en terme de fonctionalités et de facilité d'utilisation. En développement depuis plus de 10 ans, le GIMP compte aujourd'hui plusieurs millions d'utilisateurs dans le monde. Fonctionnant aussi bien en environnement Linux que Windows ou MacOSX, GIMP est utilisable pour une grande variété de tâches de manipulations d'images comme la retouche photo, la composition ou la création d'images.</p>

<p>CPE Lyon est l'Ecole Supérieure de Chimie et Physique Industrielle. Elle diplôme tous les ans 150 ingénieurs dans les domaines de l'électronique, des télécommunications et de l'informatique ainsi que 150 ingénieurs chimistes. Elle compte parmi son personnel et ses étudiants plusieurs contributeurs importants à divers logiciels libres et co-organise depuis deux ans les "Journées du Logiciel Libre" avec l'ALDIL.</p>

<p>L'ALDIL est l'Association Lyonnaise pour le Développement de l'Informatique Libre fondée en 1998 et qui a pour vocation de promouvoir les Logiciels Libres sous toutes leurs formes qu'ils fonctionnent dans un environnement Libre (GNU/Linux, FreeBSD) ou propriétaire (Macintosh ou MS-Windows). C'est aussi le principal Groupe d'Utilisateurs de Logiciels Libres lyonnais, avec plus de 60 adhérents.</p>

